"""LLMeter endpoint wrapper for vLLM's OpenAI-compatible API.

vLLM's OpenAI server speaks the standard OpenAI Chat Completions API, so we
thinly extend :class:`llmeter.endpoints.OpenAICompletionEndpoint`.

The model_id / endpoint_name are parameters; callers pass whatever
``served_model_name`` they configured in the user-data (see
:class:`vllm_ec2_bench.ModelSpec.served_model_name`).
"""
from __future__ import annotations

from typing import Any

# Imports are lazy to avoid a hard runtime dep on llmeter for users who only
# want the deployer.
try:
    from llmeter.endpoints import OpenAICompletionEndpoint
    from llmeter.endpoints.openai import OpenAICompletionStreamEndpoint
except ImportError as _exc:  # pragma: no cover
    OpenAICompletionEndpoint = None  # type: ignore[misc,assignment]
    OpenAICompletionStreamEndpoint = None  # type: ignore[misc,assignment]
    _IMPORT_ERROR = _exc
else:
    _IMPORT_ERROR = None


def _require_llmeter() -> None:
    if _IMPORT_ERROR is not None:
        raise ImportError(
            "vllm_ec2_bench.endpoint requires llmeter. "
            "Install with: pip install 'vllm-ec2-bench[notebook]'."
        ) from _IMPORT_ERROR


# -----------------------------------------------------------------------------
# Non-streaming
# -----------------------------------------------------------------------------
class VLLMEndpoint(OpenAICompletionEndpoint):  # type: ignore[misc]
    """vLLM-on-EC2 endpoint, non-streaming.

    Parameters
    ----------
    base_url
        e.g. ``http://52.3.4.5:8000/v1``. Trailing slash stripped automatically.
    api_key
        Bearer token; generated by :class:`DeploymentRunner` at launch.
    model_id
        Matches ``--served-model-name`` in vLLM. Pass
        ``model_spec.served_model_name`` from your :class:`ModelSpec`.
    endpoint_name
        Display name used in LLMeter output file paths. Defaults to
        ``f"vllm-{model_id}"``.
    """

    def __init__(
        self,
        base_url: str,
        api_key: str,
        model_id: str,
        endpoint_name: str | None = None,
        **kwargs: Any,
    ) -> None:
        _require_llmeter()
        base_url = base_url.rstrip("/")
        endpoint_name = endpoint_name or f"vllm-{model_id}"
        kwargs.setdefault("timeout", 300.0)
        kwargs.setdefault("max_retries", 2)
        super().__init__(
            model_id=model_id,
            endpoint_name=endpoint_name,
            api_key=api_key,
            provider="vllm",
            base_url=base_url,
            **kwargs,
        )

    @staticmethod
    def create_payload(  # type: ignore[override]
        system_prompt: str,
        user_input: str,
        max_tokens: int = 512,
        temperature: float = 0.0,
        top_p: float = 1.0,
        **kwargs: Any,
    ) -> dict:
        """Build an OpenAI ChatCompletions payload with a system + user message."""
        payload = {
            "messages": [
                {"role": "system", "content": system_prompt},
                {"role": "user", "content": user_input},
            ],
            "max_tokens": max_tokens,
            "temperature": temperature,
            "top_p": top_p,
        }
        payload.update(kwargs)
        return payload


# -----------------------------------------------------------------------------
# Streaming variant (optional — gives accurate TTFT)
# -----------------------------------------------------------------------------
class VLLMStreamEndpoint(OpenAICompletionStreamEndpoint):  # type: ignore[misc]
    """Streaming variant of :class:`VLLMEndpoint` for accurate TTFT metrics."""

    def __init__(
        self,
        base_url: str,
        api_key: str,
        model_id: str,
        endpoint_name: str | None = None,
        **kwargs: Any,
    ) -> None:
        _require_llmeter()
        base_url = base_url.rstrip("/")
        endpoint_name = endpoint_name or f"vllm-{model_id}-stream"
        kwargs.setdefault("timeout", 300.0)
        kwargs.setdefault("max_retries", 2)
        super().__init__(
            model_id=model_id,
            endpoint_name=endpoint_name,
            api_key=api_key,
            provider="vllm",
            base_url=base_url,
            **kwargs,
        )

    create_payload = VLLMEndpoint.create_payload  # inherit the helper


__all__ = ["VLLMEndpoint", "VLLMStreamEndpoint"]
