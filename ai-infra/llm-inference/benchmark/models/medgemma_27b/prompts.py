"""Prompt + seed input used for MedGemma-27B benchmarking.

This file is the only place the MedGemma "task" is defined. The benchmark
framework reads ``SYSTEM_PROMPT`` to seed the served endpoint and
``SEED_INPUT`` to drive a smoke test.

The task: **extract a structured booking record from a travel-booking
confirmation email**. Inputs come from ``sample-data/travel/*.jsonl`` which
is generated by ``sample-data/scripts/synthesize.py``.

MedGemma-27B is a medically-tuned Gemma variant, but for this code sample
it runs on the same generic travel-booking extraction prompt as the other
models so that throughput numbers across the matrix are comparable.
"""
from __future__ import annotations


SYSTEM_PROMPT = (
    "You are a structured-data extractor. Given a travel booking confirmation "
    "email, return ONLY a valid JSON object matching this schema: "
    "{\"booking_reference\": string, \"traveler\": string, \"origin\": string, "
    "\"destination\": string, \"depart_date\": string, \"return_date\": string|null, "
    "\"total_price\": string, \"currency\": string, \"segments\": array}. Use "
    "ISO-8601 dates (YYYY-MM-DD). Use only information present in the email. "
    "Do not invent fields. If a field is not present in the email, set it to "
    "null (or an empty array for segments)."
)
"""System prompt the MedGemma endpoint will see at serve time."""


